import UIKit

extension UIColor {
    // LINEのテーマカラー
    static var lineGreen: UIColor {
        return UIColor(red: 4/255, green: 185/255, blue: 2/255, alpha: 1)
    }
    // LINEのナビバーカラー
    static var lineGray: UIColor {
        return UIColor(red: 32/255, green: 43/255, blue: 67/255, alpha: 1)
    }
}
